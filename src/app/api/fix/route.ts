export async function GET() {
  try {
    const result = await sql`
      SELECT id, page_surah FROM progress_iqro_quran WHERE page_surah LIKE '%{idx+1}%'
    `;

    let updatedCount = 0;

    for (const row of result.rows) {
      const original = row.page_surah;
      const parts = original.split(':');
      if (parts.length >= 2) {
        const surahName = parts.slice(1).join(':').trim();
        const index = surahList.findIndex(s => s.toLowerCase() === surahName.toLowerCase());
        if (index !== -1) {
          const corrected = `Surah ${index + 1}: ${surahList[index]}`;
          await sql`
            UPDATE progress_iqro_quran SET page_surah = ${corrected} WHERE id = ${row.id}
          `;
          updatedCount++;
        }
      }
    }

    return NextResponse.json({ success: true, updatedCount });
  } catch (error: any) {
    return NextResponse.json({ error: error.message }, { status: 500 });
  }
}
